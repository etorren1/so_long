#include "libft.h"

int	ft_numsize(int num)
{
	int	i;

	i = 0;
	if (num == 0)
		return (1);
	if (num < 0)
	{
		num *= -1;
		i = 1;
	}
	while (num > 0)
	{
		num /= 10;
		i++;
	}
	return (i);
}
